bool render_status(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_ln_P(PSTR("Layer default"), false);
            break;
        case 1:
            oled_write_ln_P(PSTR("Layer one"), false);
            break;
        case 2:
            oled_write_ln_P(PSTR("Layer two"), false);
            break;
        case 3:
            oled_write_ln_P(PSTR("Layer three"), false);
            break;
        default:
            oled_write_ln_P(PSTR("Undefined"), false);
    }

    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.caps_lock ? PSTR("\nCaps lock on") : PSTR("\n            "), false);

    return false;
}

oled_rotation_t oled_init_kb(oled_rotation_t rotation) {
    if (is_keyboard_master()) {
        return OLED_ROTATION_180;
    }

    return rotation;
}

bool oled_task_kb(void) {
    if (!oled_task_user()) {
        return false;
    }
    if (is_keyboard_master()) {
        render_status();
    } else {
        render_logo();
    }
    return false;
}

